static constexpr size_t COMMON_ENGLISH_COUNT = sizeof(COMMON_ENGLISH) / sizeof(COMMON_ENGLISH[0]);

inline unordered_map<string, bool>& get_english_dict() {
    static unordered_map<string, bool> dict = []() {
        unordered_map<string, bool> m(2048, 0.65);
        for (size_t i = 0; i < COMMON_ENGLISH_COUNT; i++) {
            m.insert(string(COMMON_ENGLISH[i]), true);
        }
        return m;
    }();
    return dict;
}

inline bool is_common_english_word(const char* word, size_t len) {
    if (len == 0 || len > 30) return false;
    char buf[32];
    for (size_t i = 0; i < len; i++) {
        char c = word[i];
        buf[i] = (c >= 'A' && c <= 'Z') ? c + 32 : c;
    }
    string_view sv(buf, len);
    return get_english_dict().get(sv) != nullptr;
}

